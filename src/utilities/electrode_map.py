def get_triplet_pattern_number(e1, e2, e3, array_id):
    triplet_mat = get_litke_array_triplet_mat_by_array_id(array_id)

    index = (triplet_mat[:,0]==e1) * (triplet_mat[:,1]==e2) * (triplet_mat[:,2]==e3)

    assert np.any(index), "No such triplet array id {0}".format(array_id)
    return np.argwhere(index)[0,0]+1

