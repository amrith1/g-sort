def get_litke_array_coordinates_by_array_id (array_id):

    # from George, modified Vision ElectrodeMapFactory.java:
    # the 120 um arrays have array id 3500 <= array_id < 4000
    # or have specific array id 1505, 1506

    # FIXME stone-dead awful hack to deal with Litke streaming data
    # not sure that this even generalizes
    array_id = array_id & 0xFFF

    if 1500 <= array_id < 2500:
        if array_id == 1505 or array_id == 1506:
            return LITKE_519_ARRAY_MAP_120UM
        else:
            return LITKE_519_ARRAY_MAP
    elif 500 <= array_id < 1500:
        return LITKE_512_ARRAY_MAP
    elif 3500 <= array_id < 4000:
        return LITKE_519_ARRAY_MAP_120UM
    else:
        assert False, "No such Litke array id {0}".format(array_id)

